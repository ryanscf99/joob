#!/usr/bin/env node
/**
 * Scan public/cats + public/videos and regenerate src/lib/cat-gallery.ts
 * Runs automatically before build (npm run build / Vercel).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const catsDir = path.join(root, "public", "cats");
const vidsDir = path.join(root, "public", "videos");
const outFile = path.join(root, "src", "lib", "cat-gallery.ts");

function listMedia(dir, re) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((f) => re.test(f) && !f.startsWith("."))
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: "base" }));
}

const cats = listMedia(catsDir, /\.(jpe?g|png|webp|gif)$/i);
const vids = listMedia(vidsDir, /\.mp4$/i);

const body = `/**
 * jOOB media gallery — auto-generated by scripts/gen-media-list.mjs
 * Do not edit by hand; run: npm run media:list
 */

export const CAT_PHOTOS = ${JSON.stringify(cats, null, 2)} as const;

export const CAT_VIDEOS = ${JSON.stringify(vids, null, 2)} as const;

/** Dedicated home-page hero video (from pic/front_video.MP4) */
export const FRONT_VIDEO = "/videos/front_video.mp4";

export function catSrc(file: string): string {
  return \`/cats/\${file}\`;
}

export function videoSrc(file: string): string {
  return \`/videos/\${file}\`;
}

/** Stable pick by seed (job id, index, …) so SSR + client match */
export function pickCat(seed: string | number, offset = 0): string {
  const list = CAT_PHOTOS as readonly string[];
  if (!list.length) return "";
  const s = String(seed);
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  const idx = (h + offset) % list.length;
  return catSrc(list[idx]);
}

export function pickCats(count: number, start = 0): string[] {
  const list = CAT_PHOTOS as readonly string[];
  const out: string[] = [];
  for (let i = 0; i < count && i < list.length; i++) {
    out.push(catSrc(list[(start + i) % list.length]));
  }
  return out;
}

export function pickVideo(seed: string | number, offset = 0): string {
  const list = CAT_VIDEOS as readonly string[];
  if (!list.length) return "";
  const s = String(seed);
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  const idx = (h + offset) % list.length;
  return videoSrc(list[idx]);
}

export function pickVideos(count: number, start = 0): string[] {
  const list = CAT_VIDEOS as readonly string[];
  const out: string[] = [];
  for (let i = 0; i < count && i < list.length; i++) {
    out.push(videoSrc(list[(start + i) % list.length]));
  }
  return out;
}

/** Cat TV playlist URLs (all clips under public/videos) */
export function catTvPlaylist(): string[] {
  return (CAT_VIDEOS as readonly string[]).map(videoSrc);
}

export const JOOB_LOGO = "/brand/joob-logo.png";
export const JOOB_LOGO_SM = "/brand/joob-logo-256.png";
export const JOOB_MASCOT = "/brand/mascot-hero.jpg";
export const JOOB_AVATAR = "/brand/mascot-avatar.png";
export const JOOB_FAVICON = "/brand/favicon-ico.png";
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, body);
console.log(
  `[gen-media-list] ${cats.length} photos, ${vids.length} videos → src/lib/cat-gallery.ts`
);
