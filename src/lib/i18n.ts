export function sectorLabel(lang: Lang, sector: string): string {
  const map: Record<string, DictKey> = {
    hospitality: "sectorHospitality",
    retail: "sectorRetail",
    fnb: "sectorFnb",
    "big-health": "sectorHealth",
    finance: "sectorFinance",
    tech: "sectorTech",
    mice: "sectorMice",
    education: "sectorEducation",
    other: "sectorOther",
  };
  const k = map[sector];
  return k ? t(lang, k) : sector;
}

